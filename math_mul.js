// multiplication

const val1 = 5 * 4;
console.log(`Multiplication Value:- ${val1}`);

const val2 = '6' * '3';
console.log(`String Multiplication Value:- ${val2}`);

const val3 = 'Hello ' * 'World';
console.log(`String Multiplication Value:- ${val3}`);

const val4 = '7' * 2;
console.log(`String and Number Multiplication Value:- ${val4}`);

const val5 = 4 * true; // true is treated as 1
console.log(`Number and Boolean Multiplication Value:- ${val5}`);

const val6 = 4 * false; // false is treated as 0
console.log(`Number and Boolean Multiplication Value:- ${val6}`);

const val7 = '8' * false; // false is treated as 0
console.log(`String and Boolean Multiplication Value:- ${val7}`);

const val8 = -3 * 9;
console.log(`Multiplication Value:- ${val8}`);

const var9 = '*' * 5;
console.log(`String and Number Multiplication :-${var9}`);
